package org.literacyapp.model.enums;

/**
 * Based on the EduApp4Syria specification of requirements, section 3.3.
 */
public enum WellBeingIndicator {
    
    HAPPINESS,
    ABILITY_TO_PLAY,
    COGNITION, // Concentration, memory, ability to solve a problem
    SADNESS, // Including depression, grief, crying
    STRESS_REACTIONS, // Fear, anxiety, arousal, avoidance, sleep problems, regression, etc.
    SOMATIC_HEALTH, // Self-reported stomach pains, head aches, sleep, tiredness, appetite
    INDEPENDENCE, // Helplessness, clinginess
    EMOTION_REGULATION // Ability to regulate emotions, communicate, attachment
}
